    private void initializeCalculator() {
        String displayType = (String) jComboBox1.getSelectedItem();
        if ("Table".equals(displayType)) {
            factory = new ResultDataFactory.TableResultFactory();
        } else {
            factory = new ResultDataFactory.ExtendedResultFactory();
        }
        calculator = new Calculator(factory);
    }

    private void redirectSystemOutToTextArea() {
        PrintStream printStream = new PrintStream(new OutputStream() {
            @Override
            public void write(int b) {
                jTextArea1.append(String.valueOf((char) b));
            }
        });
        System.setOut(printStream);
        System.setErr(printStream);
    }

