    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            int number = Integer.parseInt(jTextField1.getText());
            calculator.addResult(number);
            jTextArea1.setText("");
            calculator.displayResults();
            jTextField1.setText("");
        } catch (NumberFormatException e) {
            jTextArea1.append("Помилка: введіть коректне число!\n");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            calculator.save();
            jTextArea1.append("\nДані збережено успішно!\n");
        } catch (IOException e) {
            jTextArea1.append("Помилка збереження: " + e.getMessage() + "\n");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
            calculator.restore();
            jTextArea1.setText("");
            calculator.displayResults();
            jTextArea1.append("\nДані відновлено успішно!\n");
        } catch (IOException | ClassNotFoundException e) {
            jTextArea1.append("Помилка відновлення: " + e.getMessage() + "\n");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        String selected = (String) jComboBox1.getSelectedItem();
        if ("Table".equals(selected)) {
            factory = new ResultDataFactory.TableResultFactory();
        } else {
            factory = new ResultDataFactory.ExtendedResultFactory();
        }
        List<ResultData> oldResults = calculator.getResults();
        calculator = new Calculator(factory);
        calculator.getResults().addAll(oldResults);
    }//GEN-LAST:event_jComboBox1ActionPerformed

